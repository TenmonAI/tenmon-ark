/**
 * 宿名を「翼宿（よくしゅく）」形式にフォーマットする。
 * 入力: "翼" | "翼宿" → 出力: "翼宿（よくしゅく）"
 * マップにない場合はそのまま返す（「宿」は付与する）。
 */
export function formatShukuLabel(raw: string): string {
  if (!raw) return "";
  const trimmed = raw.trim();
  const kana = SHUKU_KANA_MAP[trimmed];
  // 「宿」が付いているかチェック
  const base = trimmed.endsWith("宿") ? trimmed : trimmed + "宿";
  if (kana) {
    return `${base}（${kana}）`;
  }
  return base;
}

/**
 * 宿名のふりがなだけを返す。
 * 入力: "翼" | "翼宿" → 出力: "よくしゅく"
 */
export function getShukuKana(raw: string): string {
  if (!raw) return "";
  return SHUKU_KANA_MAP[raw.trim()] || "";
}

/**
 * 宿名を正規化（「宿」付き）で返す。
 * 入力: "翼" → 出力: "翼宿"
 */
export function normalizeShukuName(raw: string): string {
  if (!raw) return "";
  const trimmed = raw.trim();
  return trimmed.endsWith("宿") ? trimmed : trimmed + "宿";
}
