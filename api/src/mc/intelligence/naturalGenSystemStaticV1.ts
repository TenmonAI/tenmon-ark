/** P3.1 KAMIYO Synapse（chat.ts と同一ロジック） */
export function buildKamiyoSynapseClauseForNatGenV1(): string {
  try {
    const ids = ["KAMIYO:WATER_DANSHARI", "KAMIYO:FIRE_KATAKAMUNA_IROHA", "KAMIYO:IMMUNITY_HEIKE"];
    const rows: unknown[] = [];
    for (const id of ids) {
      const r: unknown = dbPrepare("kokuzo", "SELECT seedId, content FROM kokuzo_seeds WHERE seedId = ? LIMIT 1").get(id);
      if (r && typeof r === "object" && r !== null && "content" in r && String((r as { content?: unknown }).content || "").trim()) {
        rows.push(r);
      }
    }
    const joined = rows
      .map((row) => String((row as { content?: unknown }).content || "").trim())
      .filter(Boolean)
      .join("\n\n");
    if (!joined) return "";
    return (
      "\n\n【直毘の理（内部法則）】\n" +
      joined +
      "\n\n【運用】\n一般論・説教・薄い共感を出さない。言い訳は裁かず受容し、今ここで出来る一手へ落とす。出典名（断捨離/カタカムナ等）は絶対に出さない。必ず短く、質問は任意（0〜1）。言い切り（余白）も許容する。"
    );
  } catch {
    return "";
  }
}
