/**
 * セキュリティフィルタ（v3.0）
 */
export function securityFilterV3(text: string): { safe: boolean; reason?: string } {
  // 禁止ワード
  const forbiddenWords = [
    "政治", "選挙", "政党", "宗教", "神", "仏", "差別", "人種", "暴力", "殺人",
    "性的", "セックス", "エロ", "住所", "電話番号", "パスワード",
  ];

  if (forbiddenWords.some(word => text.includes(word))) {
    return { safe: false, reason: "禁止ワードが含まれています" };
  }

  // LP範囲外
  const outOfScopePatterns = [
    /天気/, /ニュース/, /株価/, /為替/, /スポーツ/, /芸能/, /料理/, /レシピ/,
    /旅行/, /観光/, /ゲーム/, /アニメ/, /漫画/, /映画/, /音楽/, /本/, /小説/,
  ];

  if (outOfScopePatterns.some(pattern => pattern.test(text))) {
    return { safe: false, reason: "LP範囲外の質問です" };
  }

  // SQLインジェクション・XSS対策
  if (/(\bSELECT\b|\bINSERT\b|\bUPDATE\b|\bDELETE\b|\bDROP\b)/i.test(text)) {
    return { safe: false, reason: "不正な入力が検出されました" };
  }

  if (/<script|javascript:|onerror=/i.test(text)) {
    return { safe: false, reason: "不正な入力が検出されました" };
  }

  return { safe: true };
}

